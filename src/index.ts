/**
 * Multiplies a given number by 10 and returns the result.
 * @param n The number to be multiplied by 10
 */
export function times10 (n: number) {
  return n * 10
}

/**
 * Multiplies a given number by 10 and returns the result.
 * @param n The number to be multiplied by 10
 */
export function times100 (n: number) {
  return n * 100
}

